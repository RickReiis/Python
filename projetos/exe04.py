valor = input('Digite algo:')
print(type(valor))
print('O valor é alfabético: {}'.format(valor.isalpha()))
print('O valor está em caixa alta: {}'.format(valor.isupper()))
print('O valor está em caixa baixa: {}'.format(valor.islower()))
print('O valor é numérico: {}'.format(valor.isnumeric()))
print('O valor é alfanumérico: {}'.format(valor.isalnum()))
print('O valor é um dígito:{}'.format(valor.isdigit()))
print('O valor é decimal:{}'.format(valor.isdecimal()))
print('O valor está na tabela ascii: {}'.format(valor.isascii()))
print('O valor é um indentificador: {}'.format(valor.isidentifier()))
print('O valor pode ser escrito na tela: {}'.format(valor.isprintable()))
print('O valor é um espaço: {}'.format(valor.isspace()))
print('O valor é um título: {}'.format(valor.istitle()))






